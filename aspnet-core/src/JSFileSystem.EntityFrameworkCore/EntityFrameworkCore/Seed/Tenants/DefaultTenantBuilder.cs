using Abp.MultiTenancy;
using JSFileSystem.Editions;
using JSFileSystem.MultiTenancy;
using Microsoft.EntityFrameworkCore;
using System.Linq;

namespace JSFileSystem.EntityFrameworkCore.Seed.Tenants;

public class DefaultTenantBuilder
{
    private readonly JSFileSystemDbContext _context;

    public DefaultTenantBuilder(JSFileSystemDbContext context)
    {
        _context = context;
    }

    public void Create()
    {
        CreateDefaultTenant();
    }

    private void CreateDefaultTenant()
    {
        // Default tenant

        var defaultTenant = _context.Tenants.IgnoreQueryFilters().FirstOrDefault(t => t.TenancyName == AbpTenantBase.DefaultTenantName);
        if (defaultTenant == null)
        {
            defaultTenant = new Tenant(AbpTenantBase.DefaultTenantName, AbpTenantBase.DefaultTenantName);

            var defaultEdition = _context.Editions.IgnoreQueryFilters().FirstOrDefault(e => e.Name == EditionManager.DefaultEditionName);
            if (defaultEdition != null)
            {
                defaultTenant.EditionId = defaultEdition.Id;
            }

            _context.Tenants.Add(defaultTenant);
            _context.SaveChanges();
        }
    }
}
